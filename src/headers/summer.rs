pub fn crc32c_byte(crc: u32, data: u8) -> u32 {
    let index = (crc ^ data as u32) & 0xff;
    return (crc >> 8) ^ CRC32C_TABLE[index as usize];
}

pub fn crc32c(crc: u32, data: Vec<u8>) -> u32 {
    let mut _crc = crc;
    for byte in data {
        _crc = crc32c_byte(_crc, byte);
    }
    return _crc;
}

pub fn crc16_byte(crc: u16, data: u8) -> u16 {
    let index = (crc ^ data as u16) & 0xff;
    return ((crc >> 8) & 0xFF) ^ CRC16_TABLE[index as usize];
}
pub fn crc16(crc: u16, input: Vec<u8>) -> u16 {
    let mut _crc = crc;
    for byte in input {
        _crc = crc16_byte(_crc, byte);
    }
    return _crc;
}
